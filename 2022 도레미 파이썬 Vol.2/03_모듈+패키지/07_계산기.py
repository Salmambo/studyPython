# cal.py에 다음 함수가 있는 모듈 cal를 만들어 주세요
#     plus(a, b) : a+b를 반환하는 함수
#     minus(a, b) : a-b를 반환하는 함수
#     times(a, b) : a*b를 반환하는 함수
#     divide(a, b) : a/b를 반환하는 함수
#     mod(a, b) : a%b를 반환하는 함수
def plus(a,b):
    return a+b
def minus(a,b):
    return a-b
def times(a,b):
    return a*b
def divide(a,b):
    return a/b
def mod(a,b):
    return a%b